
def count(t, x):
    n = len(t)

    if n <= 1:
        return n

    t = sorted(t)
    t.reverse()

    b = 0
    i = 0

    while i < n:
        val = t[i]
        missing = x - val

        if i == n - 1:
            b += 1
            break

        if missing >= t[n - 1]:
            for j in range(n - 1, i + 1, -1):
                if t[j] <= missing:
                    n -= 1
                    b += 1
                    break
        else:
            b += 1
        i += 1

    return b


